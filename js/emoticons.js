// React emoticon animations
$(function() {
	$(".laugh").on({
	    mouseenter: function () {
	    	$(".laugh").attr("src", "photos/design/emoticons/Laugh_Hover.jpg");
	    },
	    mouseleave: function () {
	        $(".laugh").attr("src", "photos/design/emoticons/Laugh_Static.jpg");
	    },
	    mousedown: function() {
	    	$(".laugh").attr("src", "photos/design/emoticons/Laugh_Down.jpg");
	    },
	    mouseup: function() {
	    	$(".laugh").attr("src", "photos/design/emoticons/Laugh_Hover.jpg");
	    }
	});
});
